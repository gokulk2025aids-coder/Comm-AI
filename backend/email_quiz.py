def get_all_questions():
    """Return all quiz questions"""
    return QUIZ_QUESTIONS

def get_random_questions(count=10):
    """Return random subset of questions"""
    import random
    return random.sample(QUIZ_QUESTIONS, min(count, len(QUIZ_QUESTIONS)))

def check_answer(question_id, selected_option):
    """Check if answer is correct"""
    question = next((q for q in QUIZ_QUESTIONS if q["id"] == question_id), None)
    if not question:
        return None
    
    return {
        "correct": selected_option == question["correct"],
        "correct_answer": question["correct"],
        "explanation": question["explanation"]
    }

def calculate_score(answers):
    """Calculate quiz score"""
    correct = sum(1 for a in answers if a.get("correct", False))
    total = len(answers)
    percentage = (correct / total * 100) if total > 0 else 0
    
    # Determine grade
    if percentage >= 90:
        grade = "A"
        message = "Excellent! You're an email etiquette expert!"
    elif percentage >= 80:
        grade = "B"
        message = "Great job! You have strong email skills."
    elif percentage >= 70:
        grade = "C"
        message = "Good! Keep learning to improve further."
    elif percentage >= 60:
        grade = "D"
        message = "Fair. Review the best practices to improve."
    else:
        grade = "F"
        message = "Needs improvement. Study the best practices library."
    
    return {
        "correct": correct,
        "total": total,
        "percentage": round(percentage, 1),
        "grade": grade,
        "message": message
    }
