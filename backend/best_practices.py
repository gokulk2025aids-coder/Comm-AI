def get_all_practices():
    """Return all best practices organized by category"""
    return BEST_PRACTICES_DATA

def get_by_category(category):
    """Get practices by category"""
    category_map = {
        "email_etiquette": BEST_PRACTICES_DATA["email_etiquette"],
        "case_studies": BEST_PRACTICES_DATA["case_studies"],
        "dos_donts": BEST_PRACTICES_DATA["dos_donts"],
        "industry_guides": BEST_PRACTICES_DATA["industry_guides"],
        "quick_tips": BEST_PRACTICES_DATA["quick_tips"]
    }
    return category_map.get(category, [])

def search_practices(query):
    """Search across all practices"""
    query = query.lower()
    results = []
    
    for category, items in BEST_PRACTICES_DATA.items():
        for item in items:
            item_str = str(item).lower()
            if query in item_str:
                results.append({**item, "source_category": category})
    
    return results
